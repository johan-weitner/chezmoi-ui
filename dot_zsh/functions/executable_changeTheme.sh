# function that takes a number, looks up the string in the array above with the corresponding index and changes enviropnment variable OMP_THEME with the looked up stringt value
function change_omp_theme() {
  echo "Changing theme"
  if [ "$1" -ge 0 ] && [ "$1" -lt 127 ]; then
    echo "Changing OMP theme to ${themes[$1]}"
    export OMP_THEME="${themes[$1]}"
    eval "$(oh-my-posh init zsh --config "$(brew --prefix oh-my-posh)/themes/$OMP_THEME.omp.json")"
  else
    echo "Invalid theme number. Please choose a number between 0 and 126"
  fi
}
